def parse_class_tokens(raw_values: Sequence[str] | None) -> List[str]:
    if not raw_values:
        return []
    out: List[str] = []
    seen = set()
    for raw in raw_values:
        for token in str(raw).split(","):
            name = token.strip()
            if not name:
                continue
            key = name.lower()
            if key in seen:
                continue
            seen.add(key)
            out.append(name)
    return out


def ensure_dataset_layout(dataset_dir: Path) -> None:
    required = [
        dataset_dir / "train" / "_annotations.coco.json",
        dataset_dir / "valid" / "_annotations.coco.json",
    ]
    missing = [str(p) for p in required if not p.exists()]
    if missing:
        joined = "\n".join(missing)
        raise FileNotFoundError(
            "Dataset layout is incomplete. Missing:\n"
            f"{joined}\n"
            "Run scripts/prepare_tiled_coco_dataset.py first."
        )


def load_coco_categories(ann_path: Path) -> List[Dict[str, Any]]:
    payload = json.loads(ann_path.read_text(encoding="utf-8"))
    categories = payload.get("categories", [])
    if not categories:
        raise ValueError(f"No categories found in {ann_path}")
    categories = sorted(categories, key=lambda x: int(x["id"]))
    return categories


def summarize_split(dataset_dir: Path, split: str) -> str:
    ann_path = dataset_dir / split / "_annotations.coco.json"
    if not ann_path.exists():
        return f"{split}: not found"
    payload = json.loads(ann_path.read_text(encoding="utf-8"))
    return (
        f"{split}: images={len(payload.get('images', []))}, "
        f"annotations={len(payload.get('annotations', []))}, "
        f"categories={len(payload.get('categories', []))}"
    )


def resolve_selected_class_names(
    all_class_names: Sequence[str],
    include_tokens: Sequence[str],
    exclude_tokens: Sequence[str],
) -> List[str]:
    lowered_to_name = {name.lower(): name for name in all_class_names}

    missing_include = [n for n in include_tokens if n.lower() not in lowered_to_name]
    if missing_include:
        raise ValueError(
            "Unknown class name(s) in --include-classes: " + ", ".join(missing_include)
        )

    missing_exclude = [n for n in exclude_tokens if n.lower() not in lowered_to_name]
    if missing_exclude:
        print(
            "Warning: unknown class name(s) in --exclude-classes ignored: "
            + ", ".join(missing_exclude)
        )

    if include_tokens:
        selected = [lowered_to_name[n.lower()] for n in include_tokens]
    else:
        selected = list(all_class_names)

    excluded = {n.lower() for n in exclude_tokens}
    selected = [name for name in selected if name.lower() not in excluded]
    if not selected:
        raise ValueError("No classes left after include/exclude filtering.")
    return selected


def build_filtered_dataset(
    source_dataset_dir: Path,
    filtered_dataset_dir: Path,
    selected_class_names: Sequence[str],
    overwrite: bool,
) -> Path:
    if filtered_dataset_dir.exists():
        if not overwrite:
            raise FileExistsError(
                f"{filtered_dataset_dir} already exists. Use --overwrite-filtered to recreate."
            )
        shutil.rmtree(filtered_dataset_dir)
    filtered_dataset_dir.mkdir(parents=True, exist_ok=True)

    selected_set = set(selected_class_names)
    name_to_new_id = {name: idx + 1 for idx, name in enumerate(selected_class_names)}
    global_categories = [
        {"id": idx + 1, "name": name, "supercategory": "defect"}
        for idx, name in enumerate(selected_class_names)
    ]

    for split in ("train", "valid", "test"):
        src_ann = source_dataset_dir / split / "_annotations.coco.json"
        if not src_ann.exists():
            continue

        payload = json.loads(src_ann.read_text(encoding="utf-8"))
        src_split_dir = source_dataset_dir / split
        old_id_to_name = {
            int(cat["id"]): str(cat["name"]) for cat in payload.get("categories", [])
        }
        old_id_to_new_id = {
            old_id: name_to_new_id[name]
            for old_id, name in old_id_to_name.items()
            if name in selected_set
        }

        filtered_images = []
        for img in payload.get("images", []):
            new_img = copy.deepcopy(img)
            raw_name = str(new_img.get("file_name", "")).strip()
            if not raw_name:
                continue
            raw_path = Path(raw_name)
            if raw_path.is_absolute():
                abs_img_path = raw_path
            else:
                abs_img_path = (src_split_dir / raw_name).resolve()
            new_img["file_name"] = str(abs_img_path)
            filtered_images.append(new_img)

        filtered_annotations = []
        for ann in payload.get("annotations", []):
            old_cat_id = int(ann.get("category_id", -1))
            if old_cat_id not in old_id_to_new_id:
                continue
            new_ann = copy.deepcopy(ann)
            new_ann["category_id"] = old_id_to_new_id[old_cat_id]
            filtered_annotations.append(new_ann)

        filtered_payload = copy.deepcopy(payload)
        filtered_payload["images"] = filtered_images
        filtered_payload["categories"] = global_categories
        filtered_payload["annotations"] = filtered_annotations

        dst_split_dir = filtered_dataset_dir / split
        dst_split_dir.mkdir(parents=True, exist_ok=True)
        (dst_split_dir / "_annotations.coco.json").write_text(
            json.dumps(filtered_payload, ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
    return filtered_dataset_dir


def prepare_dataset_for_class_selection(
    source_dataset_dir: Path,
    output_dir: Path,
    include_tokens: Sequence[str],
    exclude_tokens: Sequence[str],
    overwrite_filtered: bool,
) -> tuple[Path, List[str]]:
    categories = load_coco_categories(source_dataset_dir / "train" / "_annotations.coco.json")
    all_class_names = [str(c["name"]) for c in categories]
    selected_class_names = resolve_selected_class_names(
        all_class_names=all_class_names,
        include_tokens=include_tokens,
        exclude_tokens=exclude_tokens,
    )

    selection_meta = {
        "source_dataset_dir": str(source_dataset_dir),
        "all_class_names": all_class_names,
        "selected_class_names": selected_class_names,
        "include_classes": list(include_tokens),
        "exclude_classes": list(exclude_tokens),
    }

    if selected_class_names == all_class_names:
        selection_meta["filtered_dataset_dir"] = None
        (output_dir / "class_selection.json").write_text(
            json.dumps(selection_meta, ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        return source_dataset_dir, selected_class_names

    filtered_dataset_dir = output_dir / "_filtered_dataset"
    filtered_dataset_dir = build_filtered_dataset(
        source_dataset_dir=source_dataset_dir,
        filtered_dataset_dir=filtered_dataset_dir,
        selected_class_names=selected_class_names,
        overwrite=overwrite_filtered,
    )
    selection_meta["filtered_dataset_dir"] = str(filtered_dataset_dir)
    (output_dir / "class_selection.json").write_text(
        json.dumps(selection_meta, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    return filtered_dataset_dir, selected_class_names


def unwrap_dataset_cfg(dataset_cfg: MutableMapping[str, Any]) -> MutableMapping[str, Any]:
    cur = dataset_cfg
    while isinstance(cur, MutableMapping) and "dataset" in cur:
        next_cfg = cur.get("dataset")
        if not isinstance(next_cfg, MutableMapping):
            break
        cur = next_cfg
    return cur


def set_dataset_cfg(
    cfg: MutableMapping[str, Any],
    ann_file: Path,
    img_dir: Path,
    classes: Sequence[str],
    is_train: bool,
    image_size: int,
    use_augment: bool,
    albu_transforms: List[Dict[str, Any]] | None,
) -> None:
    dataset_cfg = unwrap_dataset_cfg(cfg)
    dataset_cfg["type"] = "CocoDataset"
    dataset_cfg["ann_file"] = str(ann_file.resolve())
    dataset_cfg["data_prefix"] = {"img": str(img_dir.resolve()) + os.sep}
    dataset_cfg["metainfo"] = {"classes": tuple(classes)}
    if is_train:
        dataset_cfg["filter_cfg"] = {"filter_empty_gt": False, "min_size": 1}

    if is_train:
        train_pipeline: List[Dict[str, Any]] = [
            {"type": "LoadImageFromFile"},
            {"type": "LoadAnnotations", "with_bbox": True},
            {"type": "Resize", "scale": (image_size, image_size), "keep_ratio": False},
        ]
        if use_augment and albu_transforms:
            train_pipeline.append(
                {
                    "type": "Albu",
                    "transforms": albu_transforms,
                    "bbox_params": {
                        "type": "BboxParams",
                        "format": "pascal_voc",
                        "label_fields": ["gt_bboxes_labels"],
                        "min_visibility": 0.0,
                        "filter_lost_elements": False,
                    },
                    "keymap": {"img": "image", "gt_bboxes": "bboxes"},
                    "skip_img_without_anno": False,
                }
            )
        else:
            train_pipeline.append({"type": "RandomFlip", "prob": 0.5})
        train_pipeline.append({"type": "PackDetInputs"})
        dataset_cfg["pipeline"] = train_pipeline
    else:
        dataset_cfg["pipeline"] = [
            {"type": "LoadImageFromFile"},
            {"type": "Resize", "scale": (image_size, image_size), "keep_ratio": False},
            {"type": "LoadAnnotations", "with_bbox": True},
            {
                "type": "PackDetInputs",
                "meta_keys": (
                    "img_id",
                    "img_path",
                    "ori_shape",
                    "img_shape",
                    "scale_factor",
                ),
            },
        ]


