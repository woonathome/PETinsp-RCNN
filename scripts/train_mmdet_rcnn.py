def recursively_set_num_classes(node: Any, num_classes: int) -> int:
    updated = 0
    if isinstance(node, MutableMapping):
        for key, value in list(node.items()):
            if key == "num_classes" and isinstance(value, int) and value > 0:
                node[key] = num_classes
                updated += 1
            updated += recursively_set_num_classes(value, num_classes)
    elif isinstance(node, list):
        for item in node:
            updated += recursively_set_num_classes(item, num_classes)
    return updated


def set_small_object_anchor_scales(model_cfg: MutableMapping[str, Any], scales: Sequence[int]) -> bool:
    rpn_head = model_cfg.get("rpn_head")
    if not isinstance(rpn_head, MutableMapping):
        return False
    anchor_generator = rpn_head.get("anchor_generator")
    if not isinstance(anchor_generator, MutableMapping):
        return False
    anchor_generator["scales"] = list(scales)
    anchor_generator.pop("octave_base_scale", None)
    anchor_generator.pop("scales_per_octave", None)
    return True


def rescale_schedule(cfg: MutableMapping[str, Any], target_epochs: int) -> None:
    train_cfg = cfg.get("train_cfg", {})
    if not isinstance(train_cfg, MutableMapping):
        return
    original_max_epochs = int(train_cfg.get("max_epochs", target_epochs))
    train_cfg["max_epochs"] = int(target_epochs)
    train_cfg["val_interval"] = 1

    schedulers = cfg.get("param_scheduler", None)
    if not isinstance(schedulers, list):
        return

    for sch in schedulers:
        if not isinstance(sch, MutableMapping):
            continue
        if "milestones" in sch and isinstance(sch["milestones"], list):
            new_milestones = []
            for m in sch["milestones"]:
                scaled = int(round(float(m) / max(1, original_max_epochs) * target_epochs))
                scaled = max(1, min(target_epochs - 1, scaled))
                new_milestones.append(scaled)
            sch["milestones"] = sorted(set(new_milestones))
        if "end" in sch and isinstance(sch["end"], int) and sch["end"] == original_max_epochs:
            sch["end"] = int(target_epochs)


def find_mmdet_config_from_installed_package(rel_path: str) -> Path:
    import mmdet  # type: ignore

    mmdet_pkg_dir = Path(mmdet.__file__).resolve().parent
    candidates = [
        mmdet_pkg_dir / ".mim" / "configs" / rel_path,
        mmdet_pkg_dir.parent / ".mim" / "configs" / rel_path,
        mmdet_pkg_dir.parents[1] / ".mim" / "configs" / rel_path,
    ]
    for path in candidates:
        if path.exists():
            return path
    raise FileNotFoundError(
        f"Could not find config '{rel_path}' in installed mmdet package.\n"
        "Please check your mmdet installation or pass --base-config explicitly."
    )


def resolve_checkpoint_for_test(args: argparse.Namespace, output_dir: Path) -> str:
    if args.resume_from is not None:
        return str(args.resume_from.resolve())
    if args.load_from:
        return args.load_from
    latest = output_dir / "latest.pth"
    if latest.exists():
        return str(latest.resolve())
